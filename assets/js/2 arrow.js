suma = (a, b) => a + b; // Es la asignación de una función flecha a la variable suma. La función flecha toma dos parámetros a y b, y devuelve la suma de estos dos parámetros.

let result = suma(6, 3);
console.log(result); //Imprimira como resultado 9
